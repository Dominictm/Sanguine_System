// Имя дисциплины из листа («Прорицание», «Auspex», «Прорицание (Auspex)» …) → ключ RULES_DISCIPLINES.
function v20DisciplineKey(name) {
  const norm = String(name || '').toLowerCase().replace(/\(.*?\)/g, '').trim();
  if (!norm) return null;
  for (const [key, d] of Object.entries(RULES_DISCIPLINES)) {
    const ruName = d.name.toLowerCase();
    if (ruName === norm || norm.startsWith(ruName) || ruName.startsWith(norm)) return key;
  }
  return null;
}
function v20DisciplineInfo(name) {
  const key = v20DisciplineKey(name);
  return key ? { key, ...RULES_DISCIPLINES[key] } : null;
}

if (typeof window !== 'undefined') {
  window.RULES_DISCIPLINES = RULES_DISCIPLINES;
  window.v20DisciplineKey = v20DisciplineKey;
  window.v20DisciplineInfo = v20DisciplineInfo;
}
